
public class Konut {
        private String konut;
        private String kat;
        private int odasayisi;
        private double fiyat;

    /**
     * @return the konut
     */
    public String getKonut() {
        return konut;
    }

    /**
     * @param konut the konut to set
     */
    public void setKonut(String konut) {
        this.konut = konut;
    }

    /**
     * @return the kat
     */
    public String getKat() {
        return kat;
    }

    /**
     * @param kat the kat to set
     */
    public void setKat(String kat) {
        this.kat = kat;
    }

    /**
     * @return the odasayisi
     */
    public int getOdasayisi() {
        return odasayisi;
    }

    /**
     * @param odasayisi the odasayisi to set
     */
    public void setOdasayisi(int odasayisi) {
        this.odasayisi = odasayisi;
    }

    /**
     * @return the fiyat
     */
    public double getFiyat() {
        return fiyat;
    }

    /**
     * @param fiyat the fiyat to set
     */
    public void setFiyat(double fiyat) {
        this.fiyat = fiyat;
    }
   
}
